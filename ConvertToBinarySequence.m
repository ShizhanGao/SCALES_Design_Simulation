function n_seq = ConvertToBinarySequence(u, v)
    % ConvertToBinarySequence 将两组 m=125 的序列映射回 4m=500 的 0-1 序列
    % 输入:
    %   u: 长度 125, 取值 [-1, 0, 1]
    %   v: 长度 125, 取值 [-1, 0, 1]
    % 输出:
    %   n_seq: 长度 500 的二值序列 (由 0 和 1 组成)

    m = 125;
    n_seq = zeros(4 * m, 1); % 初始化长度为 500 的列向量
    
    for i = 1:m
        % ========================================================
        % 1. 处理序列 u
        % 映射规则: i (1~125) 对应 n 的 (1~125) 以及 (376~500)
        % ========================================================
        pos1_u = i;           % 对应 1~125
        pos2_u = 375 + i;     % 对应 376~500
        
        if u(i) == 1
            n_seq(pos1_u) = 1;
            n_seq(pos2_u) = 0;
        elseif u(i) == 0
            n_seq(pos1_u) = 1;
            n_seq(pos2_u) = 1;
        elseif u(i) == -1
            n_seq(pos1_u) = 0;
            n_seq(pos2_u) = 1;
        else
            error('u 的取值只能是 -1, 0, 1');
        end
        
        % ========================================================
        % 2. 处理序列 v
        % 映射规则: i (1~125) 对应 n 的 (126~250) 以及 (251~375)
        % ========================================================
        pos1_v = 125 + i;     % 对应 126~250
        pos2_v = 250 + i;     % 对应 251~375
        
        if v(i) == 1
            n_seq(pos1_v) = 0;
            n_seq(pos2_v) = 1;
        elseif v(i) == 0
            n_seq(pos1_v) = 1;
            n_seq(pos2_v) = 1;
        elseif v(i) == -1
            n_seq(pos1_v) = 1;
            n_seq(pos2_v) = 0;
        else
            error('v 的取值只能是 -1, 0, 1');
        end
    end
end